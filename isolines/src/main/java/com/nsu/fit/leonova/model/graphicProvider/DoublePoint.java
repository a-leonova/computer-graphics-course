package com.nsu.fit.leonova.model.graphicProvider;

public class DoublePoint {
    private final double x;
    private final double y;

    public DoublePoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
